package com.test;

public class UnaryOperator {

	public static void main(String[] args) 
	{
		int noOfApples=10;
		
		System.out.println(noOfApples--);
		System.out.println(noOfApples);
		
		System.out.println(--noOfApples);
		System.out.println(noOfApples);
		
		System.out.println(noOfApples++);
		System.out.println(noOfApples);
		
		System.out.println(++noOfApples);
		System.out.println(noOfApples);
	}

}
